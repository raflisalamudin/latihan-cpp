#include <iostream>
using namespace std;

int main(){
	
	int x, y;
	//increment(++) decrement(--)
	//post increment (setelah) - sering digunakan cetak dulu baru operasi
	x = 1;
	cout<<x++<<endl;
	cout<<x++<<endl;
	cout<<x<<endl;
	cout<<"====================="<<endl;
	
	//pre increment (sebelum) operasi dulu baru cetak
	y = 1;
	cout<<++y<<endl;
	cout<<++y<<endl;
	cout<<y<<endl;
	
	
	
}
