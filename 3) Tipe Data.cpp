#include <iostream>
using namespace std; // agar tidak perlu menuliskan std disetiap baris program

int main(){
	
	// tipe data
	/*
	string - menuliskan kalimat
	integer - bilangan bulat
	floating point - bilangan pecahan
	double floating point - bilangan pecahan
	char - karakter terbatas (1 huruf, jika lebih maka karakter terakhir yang ditampilkan)
	bool - kondisi true or false
	
	tipe_data nama_variable = value_variable;
	*/
	
	string nama_saya = "Rafli";
	int nomor_int = 10;
	float nomor_float = 10.1; // koma harus di tulid dengan titik
	double nomor_double = 10.2;
	char karakter = 'A';
	bool bolean = true;
	
	cout <<"string nama_saya : "<< nama_saya << endl;
	cout <<"nomor int : "<< nomor_int << endl;
	cout <<"nomor float : "<< nomor_float << endl;
	cout <<"nomor double : "<< nomor_double << endl;
	cout <<"char : "<< karakter << endl;
	cout <<"boolean : "<< bolean << endl;
}



















